<script setup>
  import { defineAsyncComponent } from 'vue';

  const props = defineProps({
    name: {
      type: String,
      required: true,
    },
  });

  const icon = defineAsyncComponent(() =>
    import(`../assets/svg/${props.name}.svg`)
  );

  export default {
    icon,
  }
</script>

<template>
  <component :is="icon" class="fill-current" />
</template>